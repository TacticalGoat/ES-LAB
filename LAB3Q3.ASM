Reset_Handler
	LDR R5,=SRC1
	LDR R6,=SRC2
	LDR R7,[R6] ;COUNTER
	LDR R0,[R5]
	MOV R1,#0
	MOV R2,#0
	MOV R10,#1
UP
	ADDS R1,R0
	ADDCS R2,#1
	CMP R7,R10
	SUB R7,#1
	BNE UP
	LDR R5,=RES1
	LDR R6,=RES2
	STR R1,[R5]
	STR R2,[R6]
STOP 
