	AREA RESET,DATA,READONLY
	EXPORT __Vectors

__Vectors
	DCD 0X40001000
	DCD Reset_Handler
	
	ALIGN

	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R0,= ARR
	LDR R1,= 0X0
	MOV R2,#0xA
UP
	LDR R3,[R0]
	ADD R1,R3
	ADD R0,#4	
	SUBS R2,#1
	BNE UP
	LDR R4,= DST
	STR R1,[R4]
STOP
	B STOP
	
ARR DCD 0xA,0xB,0xC,0xD,0xE,0xF,0xAA,0xBB,0xCC,0xDD

	AREA DATASEG,DATA,READWRITE
DST DCD 0
	
	END
